print("program operasi MTK")
x=float(input("masukan nilai X="))
operasi=input("operasi(%,//,*,/)=")
y=float(input("masukan nilai y="))
if operasi=="%":
     z=x%y
     print (f"c adalah:",z)
elif operasi=="//":
     z=x//y
     print (f"c adalah:",z)
elif operasi=="*":
     z=x*y
     print (f"c adalah:",z)
elif operasi=="*":
     z=x*y
     print (f"c adalah:",z)
elif operasi=="/":
     z=x/y
     print (f"c adalah:",z)